<h3>SUS League and Championships</h3>

<P>The SUS (Scottish University Sport) Archery League is currently contested by 
  seven university archery clubs, Aberdeen, Dundee, Edinburgh, Heriot-Watt, Napier, 
  St Andrews, and Strathclyde. Every team faces every other team once during the 
  course of a league season. League matches are three way matches and are mixed 
  team of 4 Portsmouths, 3 points for 1st place, 2 for 2nd, 1 for 3rd and 0 for 
  a no show. Napier joined the league in the 2005/06 season. Senior and Novice 
  leagues run in parallel. 
<P>SUS Indoor Champs are a free standing competition, held on a Portsmouth and 
  there are senior team of 4 and novice team of 3 categories, as well as individual 
  categories. Archers from all Scottish universities may enter the Championships. 
<P>SUS Outdoor Championships work in the same format in terms of senior team being 
  mixed and the top four scores used, with the novices also mixed and top three 
  used. From 2011, senior gents shoot a St George, senior ladies and novice gents 
  shoot an Albion, whilst the novice ladies shoot a Windsor. 
<h3>BUCS Championships</h3>

<P>BUCS (formerly BUSA) Indoor and Outdoor Championships involve all the university 
  clubs from around the UK - there are over 30 in total - rather than just those 
  in Scotland. BUSA Indoors follows the same format as SUS Indoors. From 2007, 
  at BUSA Outdoors, senior gents shoot a Gents FITA, senior ladies/novice gents 
  shoot a Ladies FITA/Metric I, novice ladies shoot a Metric II. Because of the 
  difference in round novice scores do not count towards the senior team. 
<h3>British University Team Championship - BUTC</h3>

<P>Starting in 2003, this is easily the most colourful and entertaining shoot 
  in the student calendar year. It consists of up to 32 teams of 3 archers (up 
  to 2 teams from a uni) shooting a Bray I ranking round (half a FITA 18). The 
  teams are then ranked and shoot in a head-to-head format at hit/miss targets.
<h3>Open Competitions</h3>

<P>It's not just students that do archery. Very much not. It's good for you to get a taste of archery in the real world. Open competitions take place all year round and come in all shapes and sizes and if club archers attend these it will be more on an ad hoc basis, however if you have any ambitions at all with a bow and arrow, attending Open Competitions is a must.</P>


